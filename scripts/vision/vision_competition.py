# Cell 17 — Function to detect people, objects, and tables from one capture
# ============================================================

def detect_one_restaurant_frame(frame):
    """
    Detects:
    - people using YOLOv8m
    - tables using YOLOv8m
    - special objects using YOLO-World + few-shot CLIP

    Returns:
    - people_count
    - table_count
    - occupied_tables
    - free_tables
    - object_detections
    - object_counts
    - annotated frame
    """

    frame = cv2.resize(frame, (640, 480))
    h, w = frame.shape[:2]

    # -----------------------------
    # A) People + table detection
    # -----------------------------

    people_table_results = people_table_model.predict(
        frame,
        conf=0.20,
        iou=0.45,
        imgsz=1280,
        device="cpu",
        verbose=False
    )

    raw_people = []
    raw_tables = []

    for r in people_table_results:
        for box in r.boxes:
            cls = int(box.cls[0])
            name = people_table_model.names[cls]
            conf = float(box.conf[0])
            xyxy = box.xyxy[0].cpu().numpy().astype(int).tolist()

            if name == "person":
                raw_people.append({
                    "class": "person",
                    "confidence": conf,
                    "box": xyxy
                })

            elif name == "dining table":
                raw_tables.append({
                    "final_label": "table",
                    "final_label_display": "table",
                    "decision_source": "YOLOv8m",
                    "yolo_label": "dining table",
                    "yolo_confidence": conf,
                    "box": xyxy
                })

    people_detections = clean_person_boxes(
        raw_people,
        frame_shape=frame.shape,
        iou_threshold=0.25,
        inside_threshold=0.50
    )

    table_detections = clean_duplicate_object_boxes(
        raw_tables,
        iou_threshold=0.40
    )

    used_fallback_table = False

    if len(table_detections) == 0:
        fallback_table_box = [
            int(w * 0.05),
            int(h * 0.58),
            int(w * 0.98),
            int(h * 0.98)
        ]

        table_detections.append({
            "final_label": "table",
            "final_label_display": "table",
            "decision_source": "fallback",
            "yolo_label": "fallback table area",
            "yolo_confidence": 1.0,
            "box": fallback_table_box
        })

        used_fallback_table = True

    # -----------------------------
    # B) Special object detection
    # -----------------------------

    object_results = object_model.predict(
        frame,
        conf=0.03,
        iou=0.45,
        imgsz=1280,
        device="cpu",
        verbose=False
    )

    SPECIAL_CLASSES = [
        "sandwich",
        "slice_of_pie",
        "hot_dog",
        "crisps",
        "chocolate_chip_cookie",
        "waffle",
        "bacon",
        "crab",
        "lemon",
        "slice_of_pizza",
        "prawn",
        "tomato_ketchup_bottle",
        "yellow_mustard_bottle"
    ]

    FEWSHOT_THRESHOLD = 0.78
    FEWSHOT_MARGIN = 0.05

    raw_object_detections = []

    for r in object_results:
        for box in r.boxes:
            cls_id = int(box.cls[0])
            yolo_conf = float(box.conf[0])
            xyxy = box.xyxy[0].cpu().numpy().astype(int).tolist()

            yolo_label_raw = object_results[0].names[cls_id]
            yolo_label_norm = normalize_item_name(yolo_label_raw)

            area_ratio = box_area_ratio(xyxy, frame)

            # Ignore very large boxes because they often include person/background
            if area_ratio > 0.35:
                continue

            crop = crop_box_from_frame(frame, xyxy, padding=8)

            fewshot_label = None
            fewshot_score = None
            fewshot_margin = None
            fewshot_top_scores = []

            final_label = yolo_label_norm
            decision_source = "YOLO-World"

            # Few-shot correction using your saved Drive examples
            if crop is not None and len(reference_embeddings) > 0:
                best_result, all_scores = classify_image_with_few_shot(crop, top_k=20)

                filtered_scores = [
                    (label, score)
                    for label, score in all_scores
                    if label in SPECIAL_CLASSES
                ]

                filtered_scores = sorted(filtered_scores, key=lambda x: x[1], reverse=True)
                fewshot_top_scores = filtered_scores[:5]

                if len(filtered_scores) > 0:
                    fewshot_label, fewshot_score = filtered_scores[0]

                    if len(filtered_scores) > 1:
                        fewshot_margin = fewshot_score - filtered_scores[1][1]
                    else:
                        fewshot_margin = fewshot_score

                    if fewshot_score >= FEWSHOT_THRESHOLD and fewshot_margin >= FEWSHOT_MARGIN:
                        final_label = fewshot_label
                        decision_source = "Few-shot CLIP"

            useful_label = final_label in SPECIAL_CLASSES or final_label == "bottle"

            if useful_label:
                raw_object_detections.append({
                    "final_label": final_label,
                    "final_label_display": display_label_name(final_label),
                    "decision_source": decision_source,
                    "yolo_label": yolo_label_raw,
                    "yolo_label_norm": yolo_label_norm,
                    "yolo_confidence": yolo_conf,
                    "fewshot_label": fewshot_label,
                    "fewshot_label_display": None if fewshot_label is None else display_label_name(fewshot_label),
                    "fewshot_score": fewshot_score,
                    "fewshot_margin": fewshot_margin,
                    "fewshot_top_scores": fewshot_top_scores,
                    "area_ratio": area_ratio,
                    "box": xyxy
                })

    object_detections = clean_duplicate_object_boxes(
        raw_object_detections,
        iou_threshold=0.35
    )

    object_counts = Counter([obj["final_label"] for obj in object_detections])

    # -----------------------------
    # C) Table occupation
    # -----------------------------

    table_statuses = []

    occupied_tables = 0
    free_tables = 0

    for idx, table in enumerate(table_detections):
        table_box = table["box"]

        occupied_by_person = is_person_near_table(table_box, people_detections)
        occupied_by_objects, objects_on_table = table_has_objects(table_box, object_detections)

        occupied = occupied_by_person or occupied_by_objects

        if occupied:
            status = "occupied"
            occupied_tables += 1
        else:
            status = "free"
            free_tables += 1

        table_statuses.append({
            "table_id": idx + 1,
            "status": status,
            "occupied_by_person": occupied_by_person,
            "occupied_by_objects": occupied_by_objects,
            "objects_on_table": objects_on_table,
            "box": table_box,
            "source": table["decision_source"]
        })

    people_count = len(people_detections)
    table_count = len(table_detections)

    return {
        "frame": frame,
        "people_detections": people_detections,
        "people_count": people_count,
        "table_detections": table_detections,
        "table_statuses": table_statuses,
        "table_count": table_count,
        "occupied_tables": occupied_tables,
        "free_tables": free_tables,
        "object_detections": object_detections,
        "object_counts": object_counts,
        "used_fallback_table": used_fallback_table
    }

