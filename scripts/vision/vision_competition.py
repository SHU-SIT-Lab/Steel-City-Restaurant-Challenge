# Cell 18 — Check which menu is completed
# ============================================================

def check_menu_completion(cumulative_object_counts):
    """
    Checks all menus against cumulative detected object counts.
    """

    menu_results = {}

    for menu_name, required_items in MENU_REQUIREMENTS.items():
        missing_items = {}
        completed_items = {}

        for item, required_count in required_items.items():
            detected_count = cumulative_object_counts.get(item, 0)

            if detected_count >= required_count:
                completed_items[item] = detected_count
            else:
                missing_items[item] = required_count - detected_count

        is_complete = len(missing_items) == 0

        menu_results[menu_name] = {
            "complete": is_complete,
            "missing": missing_items,
            "completed": completed_items
        }

    return menu_results


def print_cumulative_result(cumulative_object_counts, latest_people_count=None, latest_occupied_tables=None, latest_free_tables=None):
    """
    Prints clean final cumulative result.
    """

    total_objects = sum(cumulative_object_counts.values())

    print("\nCURRENT CUMULATIVE RESULT")
    print("=========================")

    if latest_people_count is not None:
        print(f"people: {latest_people_count}")

    print(f"objects count: {total_objects}")

    if total_objects == 0:
        print("objects: none")
    else:
        readable_objects = []
        for label, count in cumulative_object_counts.items():
            readable_objects.append(f"{pretty_name(label)}: {count}")

        print("objects:", ", ".join(readable_objects))

    if latest_occupied_tables is not None:
        print(f"table occupied: {latest_occupied_tables}")

    if latest_free_tables is not None:
        print(f"free table: {latest_free_tables}")

    print("\nMENU COMPLETION")
    print("===============")

    menu_results = check_menu_completion(cumulative_object_counts)

    for menu_name, result in menu_results.items():
        if result["complete"]:
            print(f"{menu_name}: COMPLETE")
        else:
            missing_readable = []
            for item, missing_count in result["missing"].items():
                missing_readable.append(f"{pretty_name(item)} x{missing_count}")

            print(f"{menu_name}: not complete | missing: {', '.join(missing_readable)}")

    complete_menus = [
        menu_name
        for menu_name, result in menu_results.items()
        if result["complete"]
    ]

    print("\nCompleted menus:", complete_menus if complete_menus else "none")

    return menu_results

