while True:
    ret, frame = cap.read()

    if not ret:
        break

    frame = cv2.resize(frame, (640, 480))
    frames.append(frame.copy())

    results = pose_model.predict(
        frame,
        conf=0.25,
        iou=0.45,
        imgsz=640,
        device="cpu",
        verbose=False
    )

    if len(results[0].boxes) == 0:
        frame_index += 1
        continue

    boxes = results[0].boxes.xyxy.cpu().numpy()
    confs = results[0].boxes.conf.cpu().numpy()

    if results[0].keypoints is None:
        frame_index += 1
        continue

    keypoints_xy = results[0].keypoints.xy.cpu().numpy()
    keypoints_conf = results[0].keypoints.conf.cpu().numpy()

    for det_idx, box in enumerate(boxes):
        conf = confs[det_idx]
        x1, y1, x2, y2 = box.astype(int).tolist()

        person_box = [x1, y1, x2, y2]
        person_h = max(1, y2 - y1)

        person_id = assign_person_id(person_box, active_tracks)

        if person_id not in active_tracks:
            active_tracks[person_id] = {
                "last_center": box_center(person_box),
                "boxes": [],
                "confidences": [],
                "left_wrist_x": [],
                "right_wrist_x": [],
                "raised_frames": 0,
                "seen_frames": 0
            }

        track = active_tracks[person_id]
        track["last_center"] = box_center(person_box)
        track["boxes"].append(person_box)
        track["confidences"].append(conf)
        track["seen_frames"] += 1

        kxy = keypoints_xy[det_idx]
        kconf = keypoints_conf[det_idx]

        # Keypoints
        left_shoulder = kxy[5]
        right_shoulder = kxy[6]
        left_wrist = kxy[9]
        right_wrist = kxy[10]

        left_shoulder_conf = kconf[5]
        right_shoulder_conf = kconf[6]
        left_wrist_conf = kconf[9]
        right_wrist_conf = kconf[10]

        shoulder_y_values = []

        if left_shoulder_conf > 0.30:
            shoulder_y_values.append(left_shoulder[1])

        if right_shoulder_conf > 0.30:
            shoulder_y_values.append(right_shoulder[1])

        if len(shoulder_y_values) > 0:
            shoulder_y = np.mean(shoulder_y_values)
        else:
            shoulder_y = y1 + 0.35 * person_h

        hand_raised_this_frame = False

        # Left wrist
        if left_wrist_conf > 0.30:
            lx, ly = left_wrist
            track["left_wrist_x"].append(float(lx))

            # In image coordinates, smaller y means higher
            if ly < shoulder_y + 0.15 * person_h:
                hand_raised_this_frame = True

        # Right wrist
        if right_wrist_conf > 0.30:
            rx, ry = right_wrist
            track["right_wrist_x"].append(float(rx))

            if ry < shoulder_y + 0.15 * person_h:
                hand_raised_this_frame = True

        if hand_raised_this_frame:
            track["raised_frames"] += 1

