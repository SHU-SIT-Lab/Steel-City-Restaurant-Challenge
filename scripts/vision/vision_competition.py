def box_center(box):
    x1, y1, x2, y2 = box
    return np.array([(x1 + x2) / 2, (y1 + y2) / 2])


def assign_person_id(box, active_tracks, max_distance=120):
    """
    Assigns a detected person to an existing track based on nearest box center.
    If no close track exists, creates a new person id.
    """

    center = box_center(box)

    best_id = None
    best_dist = float("inf")

    for person_id, track in active_tracks.items():
        last_center = track["last_center"]
        dist = np.linalg.norm(center - last_center)

        if dist < best_dist:
            best_dist = dist
            best_id = person_id

    if best_id is not None and best_dist < max_distance:
        return best_id

    new_id = len(active_tracks)
    return new_id


def count_direction_changes(values, min_step=8):
    """
    Counts left-right movement changes.
    """

    if len(values) < 4:
        return 0

    directions = []

    for i in range(1, len(values)):
        diff = values[i] - values[i - 1]

        if abs(diff) >= min_step:
            directions.append(np.sign(diff))

    if len(directions) < 2:
        return 0

    changes = 0

    for i in range(1, len(directions)):
        if directions[i] != directions[i - 1]:
            changes += 1

    return changes

