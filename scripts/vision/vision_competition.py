def classify_image_with_few_shot(pil_image, top_k=10):
    """
    Compares an image/crop with your few-shot reference embeddings.
    """

    if len(reference_embeddings) == 0:
        return None, []

    image_emb = get_clip_image_embedding(pil_image)

    scores = []

    for label, ref_emb in reference_embeddings.items():
        score = torch.matmul(image_emb, ref_emb.T).item()
        scores.append((label, score))

    scores = sorted(scores, key=lambda x: x[1], reverse=True)

    best_result = scores[0] if len(scores) > 0 else None
    top_scores = scores[:top_k]

    return best_result, top_scores


