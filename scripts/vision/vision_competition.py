def box_area(box):
    x1, y1, x2, y2 = box
    return max(0, x2 - x1) * max(0, y2 - y1)


def box_area_ratio(box, frame):
    return box_area(box) / (frame.shape[0] * frame.shape[1])


def compute_iou(box1, box2):
    x1a, y1a, x2a, y2a = box1
    x1b, y1b, x2b, y2b = box2

    inter_x1 = max(x1a, x1b)
    inter_y1 = max(y1a, y1b)
    inter_x2 = min(x2a, x2b)
    inter_y2 = min(y2a, y2b)

    inter_area = max(0, inter_x2 - inter_x1) * max(0, inter_y2 - inter_y1)

    area1 = max(1, box_area(box1))
    area2 = max(1, box_area(box2))

    union = area1 + area2 - inter_area

    return inter_area / union


def inside_ratio(inner_box, outer_box):
    x1a, y1a, x2a, y2a = inner_box
    x1b, y1b, x2b, y2b = outer_box

    inter_x1 = max(x1a, x1b)
    inter_y1 = max(y1a, y1b)
    inter_x2 = min(x2a, x2b)
    inter_y2 = min(y2a, y2b)

    inter_area = max(0, inter_x2 - inter_x1) * max(0, inter_y2 - inter_y1)

    inner_area = max(1, box_area(inner_box))

    return inter_area / inner_area


def clean_person_boxes(raw_people, frame_shape, iou_threshold=0.25, inside_threshold=0.50):
    """
    Removes duplicate person boxes.
    Important for avoiding one person being counted as 2 or 3 people.
    """

    h, w = frame_shape[:2]
    frame_area = h * w

    candidates = []

    for det in raw_people:
        box = det["box"]
        conf = det["confidence"]

        area_ratio = box_area(box) / frame_area
        x1, y1, x2, y2 = box
        height = y2 - y1

        if conf < 0.25:
            continue

        if area_ratio < 0.01:
            continue

        if height < h * 0.12:
            continue

        candidates.append(det)

    candidates = sorted(candidates, key=lambda d: d["confidence"], reverse=True)

    kept = []

    for det in candidates:
        duplicate = False

        for kept_det in kept:
            iou = compute_iou(det["box"], kept_det["box"])
            inside_1 = inside_ratio(det["box"], kept_det["box"])
            inside_2 = inside_ratio(kept_det["box"], det["box"])

            if iou >= iou_threshold or inside_1 >= inside_threshold or inside_2 >= inside_threshold:
                duplicate = True
                break

        if not duplicate:
            kept.append(det)

    return kept


def clean_duplicate_object_boxes(object_detections, iou_threshold=0.35):
    """
    Removes duplicate object boxes with same final label.
    """

    if len(object_detections) == 0:
        return []

    def detection_score(det):
        if det.get("fewshot_score") is not None:
            return det["fewshot_score"]
        return det["yolo_confidence"]

    detections = sorted(object_detections, key=detection_score, reverse=True)

    kept = []

    for det in detections:
        duplicate = False

        for kept_det in kept:
            same_label = det["final_label"] == kept_det["final_label"]
            iou = compute_iou(det["box"], kept_det["box"])
            inside_1 = inside_ratio(det["box"], kept_det["box"])
            inside_2 = inside_ratio(kept_det["box"], det["box"])

            if same_label and (iou > iou_threshold or inside_1 > 0.65 or inside_2 > 0.65):
                duplicate = True
                break

        if not duplicate:
            kept.append(det)

    return kept


