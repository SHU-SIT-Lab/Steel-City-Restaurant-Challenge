def get_clip_image_embedding(pil_image):
    """
    Creates a normalized CLIP image embedding.
    This version works correctly with transformers CLIPModel.
    """

    inputs = clip_processor(
        images=pil_image,
        return_tensors="pt"
    ).to(device)

    with torch.no_grad():
        outputs = clip_model.vision_model(
            pixel_values=inputs["pixel_values"]
        )

        image_features = outputs.pooler_output
        image_features = clip_model.visual_projection(image_features)

    image_features = F.normalize(image_features, p=2, dim=-1)

    return image_features

