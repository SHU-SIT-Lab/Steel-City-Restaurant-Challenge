def horizontal_overlap_ratio(box1, box2):
    x1a, y1a, x2a, y2a = box1
    x1b, y1b, x2b, y2b = box2

    overlap = max(0, min(x2a, x2b) - max(x1a, x1b))
    smaller_width = min(x2a - x1a, x2b - x1b)

    if smaller_width <= 0:
        return 0

    return overlap / smaller_width


def vertical_gap_between_boxes(box1, box2):
    x1a, y1a, x2a, y2a = box1
    x1b, y1b, x2b, y2b = box2

    if y2a < y1b:
        return y1b - y2a
    elif y2b < y1a:
        return y1a - y2b
    else:
        return 0


def is_person_near_table(table_box, people_detections):
    """
    A table is occupied if a person overlaps horizontally with it
    and is vertically close enough.
    """

    for person in people_detections:
        person_box = person["box"]

        h_overlap = horizontal_overlap_ratio(person_box, table_box)
        v_gap = vertical_gap_between_boxes(person_box, table_box)

        px1, py1, px2, py2 = person_box
        tx1, ty1, tx2, ty2 = table_box

        person_bottom = py2

        horizontal_condition = h_overlap > 0.20
        vertical_condition = v_gap < 150
        seated_condition = person_bottom > ty1 - 120

        if horizontal_condition and vertical_condition and seated_condition:
            return True

    return False


def object_center_inside_or_near_table(object_box, table_box, margin=70):
    ox1, oy1, ox2, oy2 = object_box
    tx1, ty1, tx2, ty2 = table_box

    cx = (ox1 + ox2) / 2
    cy = (oy1 + oy2) / 2

    return (
        tx1 - margin <= cx <= tx2 + margin and
        ty1 - margin <= cy <= ty2 + margin
    )


def table_has_objects(table_box, object_detections):
    objects_on_table = []

    for obj in object_detections:
        if object_center_inside_or_near_table(obj["box"], table_box, margin=70):
            objects_on_table.append(obj["final_label"])

    return len(objects_on_table) > 0, objects_on_table

